package main

import "fmt"

func main() {

	var abstract string = "Abstract variable"
	
	var pi float64 = 3.14159265359

	var booleanValue bool = true

	secondAbstract := "Second Abstract Variable"
	secondBooleanValue := false
	
	fmt.Println(abstract)
	fmt.Println(pi)
	fmt.Println(booleanValue)

	fmt.Println(secondBooleanValue)
	fmt.Println(secondAbstract)
}

